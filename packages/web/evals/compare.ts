// Side-by-side eval of summarize() across providers on real cached review sets.
//   bun evals/compare.ts            # outputs + latency + tokens
//   bun evals/compare.ts --judge    # adds blind LLM-judge scoring (gpt-5.4)
//   bun evals/compare.ts --judge --nano   # compare gpt-5.4-nano effort ladder
import { createOpenAI } from '@ai-sdk/openai';
import { generateObject } from 'ai';
import { z } from 'zod';
import { summarize, setOnUsage, PROVIDERS, type Provider, type ReasoningEffort, type Summary } from '../llm';
import { REASONING_EFFORTS } from '@truescore/gmaps-shared';
import baseFixtures from './fixtures.json';
import sukito from './sukito';

const JUDGE = process.argv.includes('--judge');
const NANO = process.argv.includes('--nano');

// Base fixtures (generated by make-fixtures.ts) plus the large real-world Sukito
// review set, which actually discriminates summary quality.
const fixtures = [...baseFixtures, sukito];

// A contestant is one summarize() configuration. Default: the three providers.
// --nano: gpt-5.4-nano across its reasoning-effort ladder (none/low/medium/high).
type Contestant = { label: string; provider: Provider; effort?: ReasoningEffort };
const CONTESTANTS: Contestant[] = NANO
  ? (REASONING_EFFORTS as readonly ReasoningEffort[]).map((e) => ({ label: `nano:${e}`, provider: 'openai', effort: e }))
  : (Object.keys(PROVIDERS) as Provider[]).map((p) => ({ label: p, provider: p }));
// --nano runs 6 judge pairs per fixture, so scope it to the most discriminating
// real sets to keep the run time (and token cost) sane.
const RUN_FIXTURES = NANO ? fixtures.filter((f) => f.place === 'Sukito' || f.place === 'Bar La Pilareta') : fixtures;

type Usage = { provider: Provider; call: string; inputTokens: number; outputTokens: number };
let usages: Usage[] = [];
setOnUsage((u) => usages.push(u));

type Run = { label: string; ms: number; summary: Summary; inputTokens: number; outputTokens: number };

const runOne = async (f: (typeof fixtures)[number], c: Contestant): Promise<Run> => {
  const t0 = performance.now();
  const summary = await summarize(f.place, f.reviewTexts, f.filter ?? undefined, c.provider, c.effort);
  const ms = Math.round(performance.now() - t0);
  // Tokens are keyed by provider, so they're only meaningful in provider mode
  // (every nano variant reports as 'openai'); --nano relies on ms + judge scores.
  const mine = usages.filter((u) => u.provider === c.provider);
  return {
    label: c.label,
    ms,
    summary,
    inputTokens: mine.reduce((a, u) => a + u.inputTokens, 0),
    outputTokens: mine.reduce((a, u) => a + u.outputTokens, 0),
  };
};

// Bounded-parallel map: run fn over items at most `limit` at a time, preserving
// input order. Lets the slow thinking judge run concurrently without firing
// every pair at once (which overruns the API → timeouts, as latency.ts showed).
async function mapPool<T, R>(items: T[], limit: number, fn: (x: T, i: number) => Promise<R>): Promise<R[]> {
  const out: R[] = new Array(items.length);
  let next = 0;
  await Promise.all(
    Array.from({ length: Math.min(limit, items.length) }, async () => {
      while (next < items.length) {
        const i = next++;
        out[i] = await fn(items[i]!, i);
      }
    }),
  );
  return out;
}

const judgeSchema = z.object({
  a: z.object({ grounded: z.number().int(), specific: z.number().int(), useful: z.number().int() }),
  b: z.object({ grounded: z.number().int(), specific: z.number().int(), useful: z.number().int() }),
  winner: z.enum(['A', 'B', 'tie']),
  reason: z.string(),
});

// Full gpt-5.4 (not nano/mini) run at high reasoning effort — a proper thinking
// model as the blind judge. Effort is set on the generateObject call below.
const judgeModel = createOpenAI({ apiKey: process.env.OPENAI_API_KEY })('gpt-5.4');

const judgeRuns = async (f: (typeof fixtures)[number], r0: Run, r1: Run, flip: boolean) => {
  const [a, b] = flip ? [r1, r0] : [r0, r1];
  const { object } = await generateObject({
    model: judgeModel,
    providerOptions: { openai: { reasoningEffort: 'high' } },
    schema: judgeSchema,
    prompt: `${f.reviewTexts.join('\n\n')}\n\n---\n\nTwo anonymous models summarized the reviews above for ${f.place}${f.filter ? ` (topic: "${f.filter}")` : ''}. Score each output 1-5 on: grounded (claims traceable to the reviews, nothing invented), specific (concrete details over vague adjectives), useful (helps someone decide). Then pick the overall winner.\n\nOutput A:\n${JSON.stringify(a.summary, null, 1)}\n\nOutput B:\n${JSON.stringify(b.summary, null, 1)}`,
  });
  const label = (x: 'A' | 'B' | 'tie') => (x === 'tie' ? 'tie' : (x === 'A') !== flip ? r0.label : r1.label);
  const scores = flip ? { [r0.label]: object.b, [r1.label]: object.a } : { [r0.label]: object.a, [r1.label]: object.b };
  return { scores, winner: label(object.winner), reason: object.reason };
};

const block = (r: Run) => {
  const s = r.summary;
  const hl = s.highlights.map((h) => `  - (${h.sentiment}) ${h.text}`).join('\n');
  return [
    `### ${r.label} — ${r.ms}ms, ${r.inputTokens} in / ${r.outputTokens} out`,
    `**Verdict:** ${s.verdict}`,
    `**Highlights:**\n${hl || '  (none)'}`,
    `**Items:** ${(s.items ?? []).join(', ') || '—'}`,
    `**Alternatives:** ${(s.alternatives ?? []).join(', ') || '—'}`,
    `**Value for money:** ${s.valueForMoney}/5`,
  ].join('\n\n');
};

// W/T/L record and per-criterion score sums per provider, aggregated across
// every pairwise judgment (a provider is scored once per pair it appears in).
const tally: Record<string, { w: number; t: number; l: number; g: number; s: number; u: number; n: number }> = {};
const acc = (p: string) => (tally[p] ??= { w: 0, t: 0, l: 0, g: 0, s: 0, u: 0, n: 0 });

for (const [i, f] of RUN_FIXTURES.entries()) {
  console.log(`\n${'='.repeat(72)}\n## ${f.place}${f.filter ? ` [filter: ${f.filter}]` : ''} — ${f.reviewTexts.length} reviews\n`);
  usages = [];
  const runs = await Promise.all(CONTESTANTS.map((c) => runOne(f, c)));
  for (const r of runs) console.log(block(r) + '\n');
  if (!JUDGE) continue;
  // Every contestant pair (round-robin), A/B order flipped per pair to cancel
  // position bias. Judged with bounded parallelism — the thinking judge is slow,
  // so run several at once, capped, rather than all-at-once (which times out).
  const pairs: [Run, Run][] = [];
  for (let a = 0; a < runs.length; a++) for (let b = a + 1; b < runs.length; b++) pairs.push([runs[a]!, runs[b]!]);
  const judged = await mapPool(pairs, 4, ([r0, r1], k) => judgeRuns(f, r0, r1, (i + k) % 2 === 1));
  judged.forEach(({ scores, winner, reason }, k) => {
    const [r0, r1] = pairs[k]!;
    const line = Object.entries(scores)
      .map(([p, s]) => `${p}: grounded ${s.grounded}, specific ${s.specific}, useful ${s.useful}`)
      .join(' | ');
    console.log(`### judge: ${r0.label} vs ${r1.label} (gpt-5.4 thinking, blind)\n\n${line}\n\n**Winner:** ${winner} — ${reason}\n`);
    for (const [p, s] of Object.entries(scores)) {
      const x = acc(p);
      x.g += s.grounded;
      x.s += s.specific;
      x.u += s.useful;
      x.n++;
    }
    if (winner === 'tie') {
      acc(r0.label).t++;
      acc(r1.label).t++;
    } else {
      acc(winner).w++;
      acc(winner === r0.label ? r1.label : r0.label).l++;
    }
  });
}

if (JUDGE) {
  console.log(`\n${'='.repeat(72)}\n## standings — gpt-5.4 thinking, blind, all pairs across ${RUN_FIXTURES.length} review sets\n`);
  const ranked = CONTESTANTS.map((c) => {
    const x = acc(c.label);
    return { label: c.label, x, avg: x.n ? (x.g + x.s + x.u) / x.n : 0 };
  }).sort((m, n) => n.x.w - m.x.w || n.avg - m.avg);
  for (const { label, x, avg } of ranked) {
    const a = (v: number) => (x.n ? (v / x.n).toFixed(2) : '—');
    console.log(`- **${label}** — ${x.w}W ${x.t}T ${x.l}L · grounded ${a(x.g)} · specific ${a(x.s)} · useful ${a(x.u)} · avg ${avg.toFixed(2)}/15`);
  }
  console.log(`\n**Best quality:** ${ranked[0]!.label}`);
}
